    def handle_new_scenario_execution_input(self, message: str):
        """Handle input for the execute phase of scenario subtasks under the new architecture"""
        # 🤖 Handle '/ask:' command (interactive clarification)
        if message.strip().startswith("/ask:"):
            import time
            
            question = message.strip()[5:].strip()  # remove "/ask:" prefix
            if not question:
                self.tui.conversation_panel.log_warning("Please input your question after '/ask:'")
                return
            
            # ⏱️ Check invocation interval (to prevent rate limiting)
            current_time = time.time()
            elapsed = current_time - self.last_ask_time
            
            if elapsed < self.min_ask_interval and self.last_ask_time > 0:
                wait_time = self.min_ask_interval - elapsed
                self.tui.conversation_panel.log_warning(
                    f"Please wait {wait_time:.1f} seconds before asking again (to avoid API rate limiting)"
                )
                return
            
            self.tui.conversation_panel.write_line(f"\n❓ Your question: {question}")
            
            # Check whether HumanSimulator is available
            if not self.human_simulator:
                self.tui.conversation_panel.log_error("HumanSimulator not initialized; unable to answer questions")
                return
            
            # Retrieve the current subtask's L0 instruction as the original clear instruction
            if not self.current_new_scenario:
                self.tui.conversation_panel.log_error("No scenario is currently being executed")
                return
            
            subtask_idx = self.current_new_subtask_idx
            scenario = self.current_new_scenario
            
            # Retrieve the L0 instruction (as ground truth)
            l0_instruction = scenario.get_subtask_instruction(subtask_idx, level="L0")
            
            # Retrieve the currently used instruction (may be the original instruction or a certain level of abstraction, etc.)
            current_instruction = scenario.get_subtask_instruction(subtask_idx, level=self.scenario_clarity_level)
            
            # Invoke HumanSimulator to answer the question
            try:
                self.tui.conversation_panel.write_line("🤖 HumanSimulator is thinking...")
                
                # Record invocation time
                self.last_ask_time = current_time
                
                answer = self.human_simulator.answer_question(
                    question=question,
                    original_instruction=l0_instruction,
                    obfuscated_instruction=current_instruction
                )
                
                # Display answer (with length info for debugging)
                if self.args.verbose:
                    self.tui.conversation_panel.write_line(f"[DEBUG] Answer length: {len(answer)} characters")
                
                self.tui.conversation_panel.write_line(f"💬 Answer: {answer}\n")
                self.tui.conversation_panel.log_success("✅ You may continue executing the task")
            except Exception as e:
                self.tui.conversation_panel.log_error(f"HumanSimulator answer failed: {e}")
            
            return  # Maintain current status; user can continue asking questions or complete the task
        
        # 📝 Handle '/answer:' command (answer for QA task)
        if message.strip().startswith("/answer:"):
            answer_text = message.strip()[8:].strip()  # remove "/answer:" prefix
            if not answer_text:
                self.tui.conversation_panel.log_warning("Please input your answer after '/answer:'")
                return
            
            if self.env:
                # Store as both 'agent_response' and 'user_response' for compatibility
                self.env.interaction_cache = {
                    'agent_response': answer_text,
                    'user_response': answer_text
                }
            self.tui.conversation_panel.log_success(f"answer recorded: {answer_text}")
            asyncio.create_task(self.evaluate_new_scenario_subtask())
            return
        
        # Handle traditional input method
        if self.input_prompt == "answer":
            # User-provided answer (for QA task) — compatible with legacy format (directly input answer)
            if self.env:
                # Store as both 'agent_response' and 'user_response' for compatibility
                self.env.interaction_cache = {
                    'agent_response': message,
                    'user_response': message
                }
            self.tui.conversation_panel.log_success(f"answer recorded: {message}")
            asyncio.create_task(self.evaluate_new_scenario_subtask())
            
        elif self.input_prompt == "new_scenario_done":
            # Not a QA task; requires input /done (but /done is handled by handle_command, so this line should not be reached)
            self.tui.conversation_panel.write_line(
                "Prompt: Input /done after the task is completed"
            )
        else:
            # No specific prompt, just show help
            self.tui.conversation_panel.write_line(
                "Prompt: Input /done or /answer: <answer> after the task is completed (for QA tasks)"
            )
    
    async def evaluate_task(self):
        """evaluationtask"""
        self.state = "evaluating"
        self.waiting_for_input = False
        
        self.tui.conversation_panel.log_evaluation_start()
        self.tui.log_initialization("Executing automated evaluation...")
        
        try:
            if self.current_evaluator and self.env:
                # Run evaluation in a separate thread to avoid event loop conflicts
                # Because CDP operations within evaluation also create new event loops
                loop = asyncio.get_event_loop()
                score = await loop.run_in_executor(
                    None,
                    self.current_evaluator.is_successful,
                    self.env
                )
                passed = score >= 0.99
                
                # show results
                details = f"evaluator: {self.current_evaluator.name}"
                self.tui.show_evaluation_result(score, passed, details)
                
                # Cleanup environment (also run in a separate thread)
                try:
                    await loop.run_in_executor(
                        None,
                        self.current_evaluator.tear_down,
                        self.env
                    )
                    self.tui.log_initialization("Task environment cleaned up", success=True)
                except Exception as e:
                    self.tui.conversation_panel.log_warning(f"cleanup failed: {e}")
                
                # ✅ Rebuild container (only for Shopping tasks)
                if self.container_mgr and self.current_task.app == "Shopping":
                    task_instruction = self.current_task.get_instruction(self.current_level)
                    eval_config = self.current_task.get_evaluation_config()
                    eval_types = eval_config.get('eval_types', [])
                    if self.container_mgr.task_modifies_state(task_instruction, eval_types):
                        self.tui.log_initialization("Rebuilding Shopping container...")
                        # Run container rebuild in a separate thread to avoid blocking the TUI
                        await loop.run_in_executor(
                            None,
                            self.container_mgr.rebuild_container
                        )
                        self.tui.log_initialization("Shopping container rebuilt", success=True)
                        logging.info("Shopping container rebuilt successfully")
                
            else:
                # Manual evaluation
                self.tui.conversation_panel.write_line(
                    "\n⚠️  Automated evaluation is unavailable; please manually confirm:"
                )
                self.tui.conversation_panel.write_line(
                    "   Was the task successfully completed? (input y/n)"
                )
                self.waiting_for_input = True
                self.input_prompt = "manual_eval"
                return
            
            # Ask whether to continue
            self.ask_continue()
            
        except Exception as e:
            self.tui.conversation_panel.log_error(f"evaluationfailed: {e}")
            import traceback
            if self.args.verbose:
                self.tui.conversation_panel.write_line(f"{traceback.format_exc()}")
            self.reset_state()
    
