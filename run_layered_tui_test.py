    async def execute_new_scenario_subtask_with_agent(self, subtask: dict, subtask_num: int, total_subtasks: int):
        """Use Agent to execute the subtask of the Scenario under the new architecture"""
        if not self.env:
            self.tui.conversation_panel.log_error("Environment not initialized")
            return
        
        # Clear the Agent panel and start the task
        self.tui.agent_panel.reset()
        self.tui.agent_panel.start_task()
        
        # 🆕 Reuse or create an Agent (preserving scenario context)
        create_new_agent = False
        scenario = self.current_new_scenario
        is_reset_mode = scenario.reset_mode if scenario else False
        
        if not self.agent:
            # If no agent exists, create one
            create_new_agent = True
        elif is_reset_mode:
            # ⚡ Reset mode: Clear scenario context for each subtask (subtasks are independent, with no status dependencies)
            if hasattr(self.agent, 'clear_scenario_context'):
                self.agent.clear_scenario_context()
                self.tui.conversation_panel.write_line("⚡ Reset mode: Scenario context cleared (subtasks are independent)")
        elif subtask_num == 1:
            # First subtask: Clear scenario context
            if hasattr(self.agent, 'clear_scenario_context'):
                self.agent.clear_scenario_context()
                self.tui.conversation_panel.write_line("🔄 Scenario context cleared (new Scenario start)")
        
        # Create a new agent (if needed)
        if create_new_agent:
            # 🆕 Prepare Agent create parameter
            human_sim = None
            original_goal = None
            
            # HumanSimulator and ask-enabled agents are not yet available in this release
            if self.agent_type in ["qwen3vl_ask", "qwen3vl_context_ask"] and self.scenario_clarity_level:
                self.tui.conversation_panel.log_warning("HumanSimulator is not yet available in this open-source release. Ask-enabled agents will not function.")
            
            # create Agent
            self.agent = self.create_agent(human_simulator=human_sim, original_goal=original_goal)
            if not self.agent:
                self.tui.conversation_panel.log_error("Failed to create Agent; switching back to manual mode")
                self.execution_mode = "manual"
                return
            
            # Clear scenario context at scenario start
            if subtask_num == 1 and hasattr(self.agent, 'clear_scenario_context'):
                self.agent.clear_scenario_context()
                self.tui.conversation_panel.write_line("🔄 Scenario context cleared (new scenario start)")
        
        # Get instruction (based on ambiguity level or reset mode)
        scenario = self.current_new_scenario
        subtask_idx = self.current_new_subtask_idx
        
        if is_reset_mode:
            # ⚡ Reset mode: Force use of the L0 instruction
            instruction = scenario.get_subtask_instruction(subtask_idx)  # reset_mode automatically returns L0
        elif self.scenario_clarity_level:
            # Use the instruction corresponding to the ambiguity level
            instruction = scenario.get_subtask_instruction(subtask_idx, level=self.scenario_clarity_level)
        else:
            # Use the default instruction
            instruction = subtask.get('user_instruction', subtask.get('narration', ''))
        max_steps = subtask['max_steps']
        
        self.tui.conversation_panel.show_separator("Agent Execution")
        self.tui.conversation_panel.write_line(f"🤖 Agent startexecute Subtask {subtask_num}/{total_subtasks}")
        self.tui.conversation_panel.write_line(f"📝 Instruction: {instruction}")
        self.tui.conversation_panel.write_line(f"⏱️  Max Steps: {max_steps}")
        self.tui.conversation_panel.write_line("")
        
        # execute Agent
        step_count = 0
        success = False
        
        try:
            loop = asyncio.get_event_loop()
            
            def run_agent_steps():
                nonlocal step_count, success
                # 🆕 Use the standard logging module (instead of absl_logging) to ensure output is written to file
                import logging as std_logging
                agent_logger = std_logging.getLogger('agent_steps')
                
                try:
                    self.agent.reset()
                    
                    for step in range(max_steps):
                        step_count = step + 1
                        
                        self.tui.app.call_from_thread(
                            self.tui.conversation_panel.write_line,
                            f"📍 Step {step_count}/{max_steps}"
                        )
                        
                        result = self.agent.step(goal=instruction)
                        
                        if result and result.data:
                            thought = result.data.get('thought', '')
                            if thought:
                                # Display in TUI
                                self.tui.app.call_from_thread(
                                    self.tui.agent_panel.log_thought,
                                    thought
                                )
                                # 🆕 Log to file (using standard logging)
                                agent_logger.info(f"━━━ Step {step_count} Reasoning ━━━")
                                agent_logger.info(f"Thought: {thought}")
                            
                            action_desc = result.data.get('action_description', '')
                            if action_desc:
                                # Display in TUI
                                self.tui.app.call_from_thread(
                                    self.tui.agent_panel.log_action,
                                    action_desc
                                )
                                # 🆕 Log to file (using standard logging)
                                agent_logger.info(f"Action: {action_desc}")
                            
                            # 🆕 Log the raw tool_call JSON (including specific parameters such as click(x,y))
                            tool_call_raw = result.data.get('tool_call_raw', '')
                            if tool_call_raw:
                                agent_logger.info(f"Tool Call: {tool_call_raw}")
                            
                            # 🆕 Log the complete result.data to log (for debugging)
                            agent_logger.debug(f"Full result.data: {result.data}")
                        
                        if result and result.done:
                            success = True
                            break
                        
                        import time
                        time.sleep(0.5)
                
                except Exception as e:
                    import traceback
                    error_msg = f"Agent execution error: {e}\n{traceback.format_exc()}"
                    self.tui.app.call_from_thread(
                        self.tui.conversation_panel.log_error,
                        error_msg
                    )
                    agent_logger.error(error_msg)
            
            await loop.run_in_executor(None, run_agent_steps)
            
        except Exception as e:
            self.tui.conversation_panel.log_error(f"Agent execution error: {e}")
        
        # Display execute result
        self.tui.conversation_panel.write_line("")
        if success:
            self.tui.conversation_panel.log_success(f"✅ Agent completed subtask ({step_count} steps)")
        else:
            self.tui.conversation_panel.log_warning(f"⚠️  Agent reached maximum step count ({step_count}/{max_steps})")
        
        # 🆕 Do not delete Agent; retain it for reuse in the next subtask (to preserve scenario context)
        # self.agent = None  # Commented out to retain agent
        
        # Automatically enter evaluation mode under Agent mode
        self.tui.conversation_panel.write_line("")
        self.tui.conversation_panel.write_line("🔄 Automatically starting evaluation...")
        await asyncio.sleep(0.5)
        await self.evaluate_new_scenario_subtask()
    
