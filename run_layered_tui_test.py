            logging.info(f"\n{'=' * 80}")
            logging.info(f"BATCH TEST - Task {idx}/{total_tasks}")
            logging.info(f"Task ID: {task.task_id}")
            logging.info(f"App: {task.app}")
            logging.info(f"Name: {task.name}")
