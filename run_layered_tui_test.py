    async def initialize(self):
        """Initialize the system"""
        try:
            # loadtaskconfiguration
            self.tui.log_initialization(f"Loading task configuration: {self.args.config}")
            self.loader = LayeredTaskLoader(self.args.config)
            self.tui.log_initialization(
                f"Successfully loaded {len(self.loader.get_all_tasks())} Task-Level tasks",
                success=True
            )
            
            # Load the new Scenario evaluator
            self.tui.log_initialization("Loading the new Scenario architecture...")
            self._load_new_scenario_evaluators()
            self.tui.log_initialization(
                f"Successfully loaded {len(self.new_scenario_evaluators)} new Scenario evaluators",
                success=True
            )
            
            # initializeenvironment
            if not self.args.skip_env_init:
                await self.initialize_environment()
            else:
                self.tui.conversation_panel.log_warning(
                    "skipenvironmentinitialize - Supported only in manual test mode"
                )
            
            # initializecontainermanager
            await self.initialize_container_manager()
            
            return True
            
        except Exception as e:
            self.tui.log_initialization(f"initialization failed: {e}", success=False)
            import traceback
            error_detail = traceback.format_exc()
            self.tui.conversation_panel.write_line(error_detail)
            # Return True even on failure, allowing the user to continue in manual mode
            if self.args.skip_env_init:
                self.tui.conversation_panel.log_warning(
                    "The initialize phase failed, but continuation in manual mode is possible"
                )
                return True
            return False
    
    async def initialize_environment(self):
        """initializeScenDroidenvironment"""
        self.tui.log_initialization("Initializing ScenDroid environment...")
        self.tui.log_initialization(f"   Console Port: {self.args.console_port}")
        self.tui.log_initialization(f"   gRPC Port: {self.args.grpc_port}")
        
        try:
            from scendroid.env import env_launcher
            
            self.env = env_launcher.load_and_setup_env(
                console_port=self.args.console_port,
                emulator_setup=False,
                freeze_datetime=False,
                grpc_port=self.args.grpc_port
            )
            
            self.tui.log_initialization("ScenDroid environmentinitialization successful", success=True)
            
        except Exception as e:
            self.tui.log_initialization(f"environmentinitialization failed: {e}", success=False)
            self.tui.conversation_panel.write_line("\n💡 Troubleshooting:")
            self.tui.conversation_panel.write_line("   1. Confirm the simulator is running: adb devices")
            self.tui.conversation_panel.write_line(f"   2. confirmgRPCportmatch: {self.args.grpc_port}")
            self.tui.conversation_panel.write_line(f"   3. Use --skip_env_init to skip environment initialization (manual mode only)")
            raise
    
    async def initialize_container_manager(self):
        """Initialize container manager (create independent instance based on emulator port)"""
        try:
            self.tui.log_initialization("Initializing container manager...")
            
            # Create an independent container instance for the current emulator
            console_port = self.args.console_port
            self.container_mgr = cm.ShoppingContainerManager(console_port=console_port)
            
            # Update SHOPPING environment variable to match container port
            host_ip = os.environ.get('SHOPPING', 'http://localhost:7770').split(':')[1].replace('//', '')
            shopping_url = f"http://{host_ip}:{self.container_mgr.host_port}"
            os.environ['SHOPPING'] = shopping_url
            
            if self.container_mgr.is_docker_available():
                self.tui.log_initialization(
                    f"Container management enabled - container: {self.container_mgr.docker_container}:{self.container_mgr.host_port}",
                    success=True
                )
                self.tui.log_initialization(f"   Shopping URL: {shopping_url}")
                
                # 🆕 Ensure the container exists and is running
                self.tui.log_initialization("check Shopping containerstatus...")
                loop = asyncio.get_event_loop()
                container_ready = await loop.run_in_executor(
                    None,
                    self.container_mgr.create_container_if_not_exists
                )
                
                if container_ready:
                    self.tui.log_initialization("✅ Shopping container is ready", success=True)
                else:
                    self.tui.conversation_panel.log_warning(
                        "⚠️  Failed to create Shopping container - Shopping tasks may not run"
                    )
            else:
                self.tui.conversation_panel.log_warning(
                    "Docker is unavailable - container management disabled"
                )
                self.container_mgr = None
        except Exception as e:
            self.tui.conversation_panel.log_warning(
                f"containermanagerinitialization failed: {e}"
            )
            self.container_mgr = None
    
    def _load_new_scenario_evaluators(self):
        """Load a new Scenario evaluator"""
        # Register a new Scenario evaluator
        # These evaluators are automatically loaded from scendroid/apps/scenario/__init__.py
        
        # 1-day scenarios
        one_day_scenarios = [
            ('A', 'ScenarioA_BusyMonday', 'Busy Monday'),
            ('B', 'ScenarioB_RelaxedSaturday', 'Relaxed Saturday'),
            ('C', 'ScenarioC_StudentResearchDay', 'Student Research Day'),
            ('D', 'ScenarioD_TechConferenceTrip', 'Tech Conference Trip'),
            ('E', 'ScenarioE_WeekendHiking', 'Weekend Hiking & Photography'),
        ]
        
        # 7-day scenarios
        seven_day_scenarios = [
            ('W', 'ScenarioW_WeeklyWork', 'Weekly Work (7-Day)', True),  # True = is_seven_day
            ('OMNILIFE', 'ScenarioOmniLife_7Day', 'OmniLife Complete Life (7-Day)', True),
        ]
        
        # Load 1-day scenarios
        for scenario_id, eval_name, display_name in one_day_scenarios:
            eval_class = AppRegistry.get_evaluator(eval_name)
            if eval_class:
                self.new_scenario_evaluators[scenario_id] = {
                    'id': scenario_id,
                    'name': display_name,
                    'evaluator_name': eval_name,
                    'evaluator_class': eval_class,
                    'is_seven_day': False,
                }
                self.tui.log_initialization(f"   ✅ {scenario_id}: {display_name}")
            else:
                self.tui.conversation_panel.log_warning(
                    f"   ⚠️  {scenario_id}: {eval_name} not found"
                )
        
        # Load 7-day scenarios
        for scenario_id, eval_name, display_name, is_seven_day in seven_day_scenarios:
            eval_class = AppRegistry.get_evaluator(eval_name)
            if eval_class:
                self.new_scenario_evaluators[scenario_id] = {
                    'id': scenario_id,
                    'name': display_name,
                    'evaluator_name': eval_name,
                    'evaluator_class': eval_class,
                    'is_seven_day': is_seven_day,
                }
                self.tui.log_initialization(f"   ✅ {scenario_id}: {display_name} (7-Day)")
            else:
                self.tui.conversation_panel.log_warning(
                    f"   ⚠️  {scenario_id}: {eval_name} not found"
                )
    
    def handle_user_message(self, message: str):
        """Handle user message"""
        # debugoutput
        if self.args.verbose:
            self.tui.conversation_panel.write_line(
                f"DEBUG: Received message '{message}', current status: {self.state}"
            )
        
        # 🔧 Handle special input formats (not commands, but inputs with parameters)
        # /ask: and /answer: should not be treated as commands; instead, they should be passed to the corresponding status handler function
        if message.startswith('/ask:') or message.startswith('/answer:'):
            # Pass to the appropriate handler function based on the current status
            if self.state == "new_scenario_executing":
                self.handle_new_scenario_execution_input(message)
            elif self.state == "scenario_executing":
                self.handle_subtask_execution_input(message)
            elif self.state == "executing":
                self.handle_execution_input(message)
            else:
                self.tui.conversation_panel.log_warning(
                    "/ask: and /answer: can only be used during the task execution phase"
                )
            return
        
        # Command handling
        if message.startswith('/'):
            self.handle_command(message)
            return
        
        # Handle input based on the current status
        if self.state == "idle":
            self.tui.conversation_panel.log_warning(
                "Please first select a task or scenario. Input /start (Task), /scenario (Scenario), /scenario-L (Layered), or /help to view help"
            )
        elif self.state == "selecting_task":
            self.handle_task_selection(message)
        elif self.state == "selecting_level":
            self.handle_level_selection(message)
        elif self.state == "executing":
            # User input during task execution (e.g., answering questions)
            self.handle_execution_input(message)
        elif self.state == "selecting_scenario":
            self.handle_scenario_selection(message)
        elif self.state == "selecting_scenario_execution_mode":
            # Scenario execution mode selection (manual or Agent)
            asyncio.create_task(self.handle_scenario_execution_mode_selection(message))
        elif self.state == "selecting_scenario_agent_type":
            # 🆕 Scenario Agent typeselect
            asyncio.create_task(self.handle_scenario_agent_type_selection(message))
        elif self.state == "scenario_executing":
            # Scenario subtask execution - handle answers or completion confirmation
            self.handle_subtask_execution_input(message)
        elif self.state == "new_scenario_executing":
            # New-architecture scenario subtask execution - handle answers or completion confirmation
            self.handle_new_scenario_execution_input(message)
        elif self.state == "selecting_agent_mode":
            # ✅ Agent modeselect
            asyncio.create_task(self.handle_agent_mode_selection(message))
        elif self.state == "selecting_agent_type":
            # 🆕 Agent typeselect
            asyncio.create_task(self.handle_agent_type_selection(message))
        elif self.state == "selecting_batch_level":
            # ✅ Batch test difficulty selection
            asyncio.create_task(self.handle_batch_level_selection(message))
        elif self.state == "selecting_batch_start_task":
            # ✅ Batch test starting task ID selection
            asyncio.create_task(self.handle_batch_start_task_selection(message))
        elif self.state == "selecting_layered_scenario":
            # 🆕 Layered scenario selection
            asyncio.create_task(self.handle_layered_scenario_selection(message))
        elif self.state == "selecting_scenario_level":
            # 🆕 Scenario fuzzy etc.-level selection
            asyncio.create_task(self.handle_scenario_level_selection(message))
        elif self.state == "selecting_reset_scenario":
            # ⚡ Per-Task Reset scenarioselect
            asyncio.create_task(self.handle_reset_scenario_selection(message))
        else:
            self.tui.conversation_panel.write_line(
                f"Received message: {message} (status: {self.state})"
            )
    
