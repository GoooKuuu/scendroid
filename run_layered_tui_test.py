    async def handle_batch_start_task_selection(self, message: str):
        """Handle batch test starting task ID selection"""
        user_input = message.strip()
        
        # If the user presses Enter directly or enters 0, start from the first task start
        if not user_input or user_input == "0":
            self.batch_start_task_id = None
            self.tui.conversation_panel.log_success("Will resume testing from the first task start")
            self.tui.conversation_panel.write_line("")
            await self.run_batch_test()
            return
        
        # Verify whether the input is a valid task ID
        try:
            task_id = int(user_input)
            
            # Check whether the task ID exists
            all_tasks = self.loader.get_all_tasks()
            task_ids = [task.task_id for task in all_tasks]
            
            if task_id not in task_ids:
                self.tui.conversation_panel.log_warning(
                    f"Task ID {task_id} does not exist; please enter a valid task ID ({min(task_ids)}-{max(task_ids)})" 
                )
                return
            
            self.batch_start_task_id = task_id
            self.tui.conversation_panel.log_success(f"Will resume testing from task {task_id}")
            self.tui.conversation_panel.write_line("")
            await self.run_batch_test()
            
        except ValueError:
            self.tui.conversation_panel.log_warning("Please enter a valid task ID (numeric) or press Enter directly")
            return
    
    async def run_batch_test(self):
        """Run batch test"""
        self.tui.conversation_panel.show_separator("Starting batch test")
        self.tui.conversation_panel.log_system(
            f"Running {self.batch_level}-difficulty tests on all tasks", 
            icon="🚀"
        )
        
        # resetresultlist
        self.batch_results = []
        
        # Get all tasks
        all_tasks = self.loader.get_all_tasks()
        
        # ✅ If a starting task ID has been set up, filter the task list (resuming from breakpoint)
        if self.batch_start_task_id is not None:
            # Find the index of the starting task
            start_index = None
            for idx, task in enumerate(all_tasks):
                if task.task_id == self.batch_start_task_id:
                    start_index = idx
                    break
            
            if start_index is not None:
                # Resume testing from the starting task
                all_tasks = all_tasks[start_index:]
                self.tui.conversation_panel.write_line(f"📌 Resuming from breakpoint: starting from task {self.batch_start_task_id}")
                logging.info(f"Resuming from task ID: {self.batch_start_task_id}")
        
        total_tasks = len(all_tasks)
        original_total = len(self.loader.get_all_tasks())
        
        self.tui.conversation_panel.write_line(f"Total number of tasks: {original_total}")
        if self.batch_start_task_id:
            self.tui.conversation_panel.write_line(f"This test run: {total_tasks} tasks (starting from task {self.batch_start_task_id})")
        else:
            self.tui.conversation_panel.write_line(f"This test run: {total_tasks} tasks")
        self.tui.conversation_panel.write_line(f"Test difficulty: {self.batch_level}")
        self.tui.conversation_panel.write_line(f"Max steps: dynamically configured based on task complexity (10 × complexity)")
        self.tui.conversation_panel.write_line("")
        
        logging.info("=" * 80)
        logging.info(f"BATCH TEST START")
        logging.info(f"Level: {self.batch_level}")
        logging.info(f"Total Tasks (original): {original_total}")
        logging.info(f"Tasks to test: {total_tasks}")
        if self.batch_start_task_id:
            logging.info(f"Start from task ID: {self.batch_start_task_id}")
        logging.info(f"Max Steps: {self.max_steps}")
        logging.info("=" * 80)
        
        # Test each task individually
        for idx, task in enumerate(all_tasks, 1):
            self.tui.conversation_panel.write_line("")
            self.tui.conversation_panel.show_separator(f"task {idx}/{total_tasks}")
            self.tui.conversation_panel.write_line(f"📋 Task ID: {task.task_id}")
