# 🆕 Read Shopping mode configuration (generated by init_shopping_environment.sh)
def _load_shopping_config():
    """Loaded from config Shopping-related environment variables"""
    config_file = "/tmp/shopping_mode.conf"
    if os.path.exists(config_file):
        try:
            with open(config_file, 'r') as f:
                for line in f:
                    line = line.strip()
                    # Skip comments and blank lines
                    if not line or line.startswith('#'):
                        continue
                    if '=' in line:
                        key, value = line.split('=', 1)
                        key = key.strip()
                        value = value.strip()
                        # set environment variables
                        if key in ['SHOPPING_MODE', 'SHOPPING_URL', 'SHOPPING', 'HOST_IP']:
                            os.environ[key] = value
                            if key == 'SHOPPING_URL':
                                # Also update the SHOPPING environment variable
                                os.environ['SHOPPING'] = value
                            logging.info(f"   loaded from config: {key}={value}")
        except Exception as e:
            logging.warning(f"Failed to read Shopping configuration file: {e}")

