    def _clear_clock_data(self):
        """Clear all data for the Clock app (including alarms, timers, etc.)"""
        try:
            import subprocess
            from scendroid.env import adb_utils
            from scendroid.task_evals.utils import sqlite_utils
            
            # ✅ Fix: Google Clock app package name and database path
            # Different devices may use different Clock apps
            clock_package_name = "com.google.android.deskclock"
            clock_db_path = f"/data/data/{clock_package_name}/databases/alarms.db"
            
            logging.info(f"Clearing Clock app: {clock_package_name}")
            
            # Method 1: Attempt to delete all alarms from the database
            try:
                # Delete the alarms table
                sqlite_utils.delete_all_rows_from_table(
                    "alarms", clock_db_path, self.env, "clock"
                )
                logging.info("✅ Clock alarms table cleared via SQLite")
            except FileNotFoundError as fnf_error:
                logging.warning(f"Clock database not found: {fnf_error}")
                logging.warning("Attempting to clear entire app data instead...")
                # If the database does not exist, use the method to clear app data
                try:
                    adb_utils.clear_app_data(clock_package_name, self.env.controller)
                    logging.info("✅ Clock app data cleared via clear_app_data")
                except Exception as clear_error:
                    logging.error(f"Failed to clear Clock app data: {clear_error}")
                    # Continue even if it fails, without blocking the workflow
            except subprocess.TimeoutExpired as timeout_error:
                # Launch app timeout — the Clock app may start slowly, but database operations may already be complete
                logging.warning(f"Clock app launch timed out (data may still be cleared): {timeout_error}")
                logging.info("Continuing with cleanup...")
            except Exception as db_error:
                logging.warning(f"Failed to clear alarms table: {db_error}")
                # Attempt to clear the entire app data
                try:
                    adb_utils.clear_app_data(clock_package_name, self.env.controller)
                    logging.info("✅ Clock app data cleared via clear_app_data")
                except Exception as clear_error:
                    logging.error(f"Failed to clear Clock app data: {clear_error}")
            
            # Close the Clock app to register the changes
            try:
                adb_utils.close_app("clock", self.env.controller)
                logging.info("Clock app closed")
            except Exception as close_error:
                logging.warning(f"Failed to close Clock app: {close_error}")
            
        except Exception as e:
            logging.error(f"Failed to clear Clock data: {e}")
            import traceback
            logging.error(traceback.format_exc())
    
