    async def execute_task(self):
        """executetask"""
        self.state = "executing"
        
        # Display task details
        self.tui.show_task_details(self.current_task, self.current_level)
        
        # Get and initialize evaluator
        try:
            self.tui.log_initialization("Preparing evaluator...")
            self.current_evaluator = self.loader.get_androidworld_evaluator(self.current_task)
            
            if self.current_evaluator is None:
                self.tui.log_initialization("Automatic evaluator not found; manual evaluation will be used", success=False)
            else:
                self.tui.log_initialization(
                    f"Evaluator prepared: {self.current_evaluator.name}",
                    success=True
                )
                
                # ✅ Dynamically compute max_steps (formula: 10 * complexity)
                if hasattr(self.current_evaluator, 'complexity'):
                    self.max_steps = int(10 * self.current_evaluator.complexity)
                    self.tui.log_initialization(
                        f"max steps: {self.max_steps} (complexity: {self.current_evaluator.complexity})",
                        success=True
                    )
                else:
                    self.max_steps = 20  # default value
                    self.tui.log_initialization(
                        f"max steps: {self.max_steps} (default)",
                    success=True
                )
            
            # Check whether the status will be modified
            modifies_state = False
            if self.container_mgr and self.current_task.app == "Shopping":
                task_instruction = self.current_task.get_instruction(self.current_level)
                eval_config = self.current_task.get_evaluation_config()
                eval_types = eval_config.get('eval_types', [])
                modifies_state = self.container_mgr.task_modifies_state(
                    task_instruction, eval_types
                )
                if modifies_state:
                    self.tui.conversation_panel.log_warning(
                        "This task will modify the system status; the container will be automatically rebuilt after completion"
                    )
            
            # initialize taskenvironment
            if self.current_evaluator and self.env:
                self.tui.log_initialization("Initializing task environment...")
                
                try:
                    # Run initialize_task in a separate thread to avoid event loop conflicts
                    # Because the TUI is already running in an asyncio event loop, and initialize_task
                    # internally performs CDP login, which attempts to create a new event loop
                    loop = asyncio.get_event_loop()
                    await loop.run_in_executor(
                        None, 
                        self.current_evaluator.initialize_task,
                        self.env
                    )
                    
                    self.tui.log_initialization("Task environment initialized", success=True)
                    
                    # ✅ For non-Shopping tasks, return to the Home interface to start the task
                    if self.current_task.app != "Shopping":
                        self.tui.log_initialization("Returning to the Home interface...")
                        try:
                            await loop.run_in_executor(
                                None,
                                lambda: self.env.reset(go_home=True)
                            )
                            self.tui.log_initialization("Returned to the Home interface", success=True)
                        except Exception as e:
                            self.tui.conversation_panel.log_warning(f"Failed to return to Home screen: {e}")
                    
                except Exception as init_error:
                    self.tui.conversation_panel.log_error(f"initialization failed: {init_error}")
                    import traceback
                    self.tui.conversation_panel.write_line(traceback.format_exc())
            elif not self.env:
                self.tui.conversation_panel.log_warning(
                    "Environment not initialized; skipping task initialization (WebArena task requires environment)"
                )
            elif not self.current_evaluator:
                self.tui.conversation_panel.log_warning(
                    "not foundevaluator, skiptaskinitialize"
                )
            
            # ✅ Based on execute mode: select and execute
            if self.execution_mode == "agent":
                # Agent auto execute
                await self.execute_task_with_agent()
            else:
                # Manual execution (original logic)
                self.tui.conversation_panel.show_separator("manual executionphase")
                self.tui.conversation_panel.write_line(
                    "📱 Please manually execute the task on the simulator"
                )
                self.tui.conversation_panel.write_line(
                    "💡 Follow the instruction displayed above"
                )
                
                # Check whether user-provided answer is required
                if self.current_task.requires_user_answer():
                    self.tui.conversation_panel.write_line(
                        "\n📝 This is an information retrieval task; after completion, you must provide an answer"
                    )
                    self.tui.conversation_panel.write_line(
                        "After performing the completion action, please input your answer in the input box below\n"
                    )
                    self.waiting_for_input = True
                    self.input_prompt = "answer"
                else:
                    self.tui.conversation_panel.write_line(
                        "\nAfter completion, please input '/done' to continue evaluation\n"
                    )
                    self.waiting_for_input = True
                    self.input_prompt = "done"
            
        except Exception as e:
            self.tui.log_initialization(f"Task preparation failed: {e}", success=False)
            import traceback
            if self.args.verbose:
                self.tui.conversation_panel.write_line(f"{traceback.format_exc()}")
            self.reset_state()
    
