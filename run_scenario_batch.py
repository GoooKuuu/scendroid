    async def run_single_scenario(self, scenario_key: str, start_from_subtask: int = None) -> Dict:
        """Run a single scenario test.
        
        Args:
            scenario_key: Scenario key (lowercase, e.g., "a", "omnilife")
            start_from_subtask: Optional subtask ID to start from (1-based). 
                               If specified, batch initialization still runs, 
                               but execution jumps to this subtask.
        
        Returns:
            Dictionary with test results
        """
        # Get scenario info
        if scenario_key not in SCENARIO_MAP:
            raise ValueError(f"Unknown scenario: {scenario_key}")
        
        scenario_info = SCENARIO_MAP[scenario_key]
        scenario_id = scenario_info["id"]
        scenario_name = scenario_info["name"]
        
        # Check if evaluator exists
        if scenario_id not in self.evaluators:
            raise ValueError(f"Evaluator not found for scenario {scenario_id}")
        
        # Start logging
        self.logger.start_scenario(self.agent_type, scenario_id, scenario_name)
        
        # Create evaluator instance with clarity_level and reset_mode
        evaluator_class = self.evaluators[scenario_id]
        evaluator_params = {
            'clarity_level': self.clarity_level,  # Pass clarity level
            'reset_mode': self.reset_mode,  # ⚡ Pass reset mode
        }
        evaluator = evaluator_class(evaluator_params)
        
        # Check if scenario supports layered instructions
        if self.clarity_level is not None:
            supports_layers = self._check_layered_support(evaluator)
            if not supports_layers:
                warning_msg = (
                    f"⚠️  NOTICE: Scenario '{scenario_id}' does not have L0/L1/L2 instructions.\n"
                    f"   Requested level: {self.clarity_level}\n"
                    f"   Falling back to original instructions (user_instruction)."
                )
                print(f"\n\033[33m{warning_msg}\033[0m\n")  # Yellow warning
                self.logger.log(warning_msg)
                # Continue execution with original instructions (clarity_level will be ignored)
        
        # Initialize scenario
        self.logger.log("🔧 Initializing scenario environment...")
        try:
            # Run blocking initialize_task in executor (it's not async)
            loop = asyncio.get_event_loop()
            await loop.run_in_executor(
                None,
                evaluator.initialize_task,
                self.env
            )
            self.logger.log("✅ Scenario initialized\n")
        except Exception as e:
            self.logger.log(f"⚠️  Scenario initialization failed: {e}\n")
            import traceback
            traceback.print_exc()
        
        # Create agent
        self.logger.log(f"🤖 Creating agent: {self.agent_type}")
        
        # HumanSimulator for ask-enabled agents (coming in future release)
        human_sim = None
        
        agent = create_agent(self.agent_type, self.env, original_goal="", human_sim=human_sim)
        
        # Clear scenario context if supported
        if hasattr(agent, 'clear_scenario_context'):
            agent.clear_scenario_context()
        
        self.logger.log("✅ Agent created\n")
        
        # Execute all subtasks
        subtask_results = []
        total_subtasks = len(evaluator.subtasks)
        
        # 🎯 Check if we should start from a specific subtask
        start_idx = 0
        if start_from_subtask is not None:
            # Convert to 0-based index
            start_idx = start_from_subtask - 1
            if start_idx < 0 or start_idx >= total_subtasks:
                warning_msg = (
                    f"⚠️  WARNING: Invalid subtask ID {start_from_subtask}. "
                    f"Valid range: 1-{total_subtasks}. Starting from subtask 1."
                )
                print(f"\n\033[33m{warning_msg}\033[0m\n")
                self.logger.log(warning_msg)
                start_idx = 0
            else:
                skip_msg = (
                    f"\n{'='*80}\n"
                    f"⏭️  FAST START MODE\n"
                    f"{'='*80}\n"
                    f"   • Batch initialization: ✅ Completed\n"
                    f"   • Skipping subtasks: 1-{start_from_subtask - 1}\n"
                    f"   • Starting from: Subtask {start_from_subtask}\n"
                    f"   • Total subtasks: {total_subtasks}\n"
                    f"{'='*80}\n"
                )
                print(f"\033[36m{skip_msg}\033[0m")  # Cyan info
                self.logger.log(skip_msg)
        
        for subtask_idx in range(total_subtasks):
            subtask = evaluator.subtasks[subtask_idx]
            subtask_name = subtask.get("evaluator_name", f"Subtask {subtask_idx + 1}")
            
            # 🎯 Skip subtasks before start_idx
            if subtask_idx < start_idx:
                skip_msg = f"⏭️  Skipping Subtask {subtask_idx + 1}: {subtask_name}"
                print(f"\033[90m{skip_msg}\033[0m")  # Gray
                self.logger.log(skip_msg)
                
                # Add placeholder result for skipped subtasks
                subtask_results.append({
                    "subtask_id": subtask_idx + 1,
                    "subtask_name": subtask_name,
                    "instruction": "(skipped)",
                    "success": None,
                    "score": 0.0,
                    "steps": 0,
                    "duration": 0.0,
                    "skipped": True
                })
                continue
            
            # Get instruction based on clarity level (pass level explicitly like TUI does)
            instruction = evaluator.get_subtask_instruction(subtask_idx, level=self.clarity_level)
            
            # Determine actual instruction type used
            actual_level = "Original"
            if self.clarity_level is not None and self._check_layered_support(evaluator):
                actual_level = self.clarity_level
            elif self.clarity_level is not None:
                actual_level = f"Original (requested {self.clarity_level}, not supported)"
            
            self.logger.log_subtask_start(subtask_idx, subtask_name, instruction, actual_level)
            
            # 🆕 Reset agent before each subtask (critical for clearing history)
            # This matches the behavior in run_layered_tui_test.py (Line 1527, 1945, 2452)
            # Note: Different agents have different reset() signatures:
            #   - GUI-Owl: reset(go_home=False)
            #   - Qwen3-VL: reset(go_home_on_reset=False)
            #   - GELab-Zero: reset(goal=None)
            # Solution: Call without arguments (all have default values)
            if hasattr(agent, 'reset'):
                agent.reset()
                self.logger.log("🔄 Agent reset: history cleared\n")
            
            # Execute subtask
            try:
                subtask_result = await self.execute_subtask(
                    agent, evaluator, subtask_idx, instruction
                )
                subtask_results.append(subtask_result)
                
                self.logger.log_subtask_result(
                    subtask_idx,
                    subtask_result['success'],
                    subtask_result['score'],
                    subtask_result.get('details', ''),
                    subtask_result.get('step_details', []),
                    subtask_result.get('ask_count', 0),
                    subtask_result.get('total_steps', 0)
                )
                
                # Add completed task to context if supported
                if hasattr(agent, 'add_completed_task_to_scenario_context'):
                    agent.add_completed_task_to_scenario_context(
                        subtask_name, instruction
                    )
            
            except Exception as e:
                self.logger.log(f"❌ Error in subtask: {e}")
                import traceback
                traceback.print_exc()
                
                subtask_results.append({
                    "name": subtask_name,
                    "success": False,
                    "score": 0.0,
                    "error": str(e),
                })
        
        # Evaluate overall scenario
        self.logger.log("\n📊 Evaluating overall scenario...")
        final_score = evaluator.evaluate(self.env)
        success = final_score >= 0.8  # 80% threshold
        
        # Save log
        log_path = self.logger.save_log(
            self.agent_type, scenario_id, final_score, success, subtask_results
        )
        
        return {
            "scenario": scenario_id,
            "name": scenario_name,
            "success": success,
            "score": final_score,
            "subtask_results": subtask_results,
            "log_path": str(log_path),
        }
    
    async def execute_subtask(self, agent, evaluator, subtask_idx: int, 
                              instruction: str) -> Dict:
        """Execute a single subtask with the agent.
        
        Args:
            agent: Agent instance
            evaluator: Scenario evaluator
            subtask_idx: Subtask index
            instruction: Task instruction
        
        Returns:
            Dictionary with subtask result
        """
        subtask = evaluator.subtasks[subtask_idx]
        max_steps = subtask.get("max_steps", 20)
        
        # 🆕 For ask-enabled agents, update original_goal to L0 instruction
        if hasattr(agent, 'original_goal'):
            # Try to get L0 instruction as ground truth
            try:
                l0_instruction = evaluator.get_subtask_instruction(subtask_idx, level="L0")
                agent.original_goal = l0_instruction
                self.logger.log(f"📝 Updated agent's original_goal (L0) for ask functionality\n")
            except:
                # Fallback to current instruction if L0 not available
                agent.original_goal = instruction
                self.logger.log(f"📝 Updated agent's original_goal (fallback) for ask functionality\n")
        
        # Initialize subtask
        self.logger.log("🔧 Initializing subtask...")
        try:
            # Run blocking initialize_subtask in executor (it's not async)
            loop = asyncio.get_event_loop()
            await loop.run_in_executor(
                None,
                evaluator.initialize_subtask,
                subtask_idx,
                self.env
            )
            self.logger.log("✅ Subtask initialized\n")
        except Exception as e:
            self.logger.log(f"⚠️  Subtask initialization failed: {e}\n")
            import traceback
            traceback.print_exc()
        
        self.logger.log(f"🏃 Executing with agent (max {max_steps} steps)...")
        self.logger.log("")  # Empty line for readability
        
        step_count = 0
        done = False
        step_details = []  # Store detailed step information
        
        while step_count < max_steps and not done:
            step_count += 1
            
            # Print step header in terminal (with color)
            step_header = f"{'─'*80}\n📍 Step {step_count}/{max_steps}\n{'─'*80}"
            print(f"\033[36m{step_header}\033[0m")  # Cyan color
            self.logger.log(step_header)
            
            try:
                # Agent step
                response = agent.step(instruction)
                
                # Extract thought and action from response
                thought = ""
                action = ""
                action_type = ""
                tool_call = ""
                model_output = ""  # 🆕 Raw model output
                dual_instruction = ""  # 🆕 Instruction for dual model
                
                # 🆕 Dual-model-specific field (initialize is empty)
                planner_reasoning = ""
                planner_instruction = ""
                executor_thought = ""
                executor_action = ""
                
                # 🆕 Ask-specific field (initialize is empty)
                question = ""
                answer = ""
                
                if hasattr(response, 'data') and response.data:
                    thought = response.data.get('thought', '')
                    action = response.data.get('action_description', '')
                    action_type = response.data.get('action_type', '')
                    tool_call = response.data.get('tool_call_raw', '')
                    model_output = response.data.get('action_output', '')  # Original output
                    
                    # 🆕 For dual model, extract additional information
                    # dual_context_agent uses: planner_reasoning, planner_instruction, executor_thought, executor_action
                    planner_reasoning = response.data.get('planner_reasoning', '')
                    planner_instruction = response.data.get('planner_instruction', '')
                    executor_thought = response.data.get('executor_thought', '')
                    executor_action = response.data.get('executor_action', '')
                    
                    # 🆕 For ask agent, extract the question and answer
                    question = response.data.get('question', '')
                    answer = response.data.get('answer', '')
                    
                    # Set up dual_instruction (preferentially use planner_instruction)
                    dual_instruction = planner_instruction or response.data.get('instruction', '')
                
                # Print to terminal with formatting
                # 🆕 Distinguish output types: ask, dual model, single model
                is_ask_action = (action_type == 'ask')
                is_dual_model = bool(planner_reasoning or planner_instruction)
                
                if is_ask_action:
                    # Ask mode: display the question and answer
                    if question:
                        question_output = f"❓ Question: {question}"
                        print(f"\033[33m{question_output}\033[0m")  # Yellow
                        self.logger.log(question_output, also_print=False)
                    
                    if answer:
                        answer_output = f"💬 Answer: {answer}"
                        print(f"\033[32m{answer_output}\033[0m")  # Green
                        self.logger.log(answer_output, also_print=False)
                    
                    if action:
                        action_output = f"🎯 Action: {action}"
                        print(f"\033[36m{action_output}\033[0m")  # Cyan
                        self.logger.log(action_output, also_print=False)
                
                elif is_dual_model:
                    # Dual Model: output only the Planner and Executor fields
                    if planner_reasoning:
                        reasoning_output = f"🧠 Planner Reasoning: {planner_reasoning}"
                        print(f"\033[35m{reasoning_output}\033[0m")  # Magenta (colored)
                        self.logger.log(reasoning_output, also_print=False)  # Log only, do not print
                    
                    if planner_instruction:
                        instruction_output = f"📋 Planner Instruction: {planner_instruction}"
                        print(f"\033[36m{instruction_output}\033[0m")  # Cyan
                        self.logger.log(instruction_output, also_print=False)
                    
                    if executor_thought:
                        exec_thought = f"💡 Executor Thought: {executor_thought}"
                        print(f"\033[33m{exec_thought}\033[0m")  # Yellow
                        self.logger.log(exec_thought, also_print=False)
                    
                    if executor_action:
                        exec_action = f"⚡ Executor Action: {executor_action}"
                        print(f"\033[32m{exec_action}\033[0m")  # Green
                        self.logger.log(exec_action, also_print=False)
                else:
                    # Single model: output only thought and action
                    if thought:
                        thought_output = f"💭 Thought: {thought}"
                        print(f"\033[33m{thought_output}\033[0m")  # Yellow
                        self.logger.log(thought_output, also_print=False)
                    
                    if action:
                        action_output = f"🎯 Action: {action}"
                        print(f"\033[32m{action_output}\033[0m")  # Green
                        self.logger.log(action_output, also_print=False)
                
                # Tool Call (output by all models)
                if tool_call:
                    tool_output = f"🔧 Tool Call: {tool_call}"
                    print(f"\033[90m{tool_output}\033[0m")  # Gray color
                    self.logger.log(tool_output, also_print=False)
                
                # 🆕 Original model output is no longer logged (too long and unnecessary)
                # if model_output:
                #     self.logger.log(f"📤 Model Output (raw): {model_output[:500]}...")
                
                # 🆕 Save screenshot and get relative path  
                screenshot_path = None
                raw_screenshot = None
                if hasattr(response, 'data') and response.data:
                    raw_screenshot = response.data.get('raw_screenshot', None)
                
                if raw_screenshot is not None:
                    screenshot_path = self.logger.save_screenshot(
                        raw_screenshot, subtask_idx, step_count
                    )
                    if screenshot_path:
                        print(f"📸 Screenshot saved: {screenshot_path}")
                
                # 🆕 Store step details with enhanced information
                step_detail = {
                    "step": step_count,
                    "thought": thought,
                    "action": action,
                    "action_type": action_type,
                    "tool_call": tool_call,
                    "screenshot_path": screenshot_path,
                    # Model input and original output are no longer logged (too redundant)
                }
                
                # 🆕 If it is an ask type, add the question and answer
                if action_type == 'ask':
                    step_detail["question"] = question
                    step_detail["answer"] = answer
                
                # 🆕 If it is a dual model, add complete information
                if planner_reasoning or planner_instruction:
                    step_detail["planner_reasoning"] = planner_reasoning
                    step_detail["planner_instruction"] = planner_instruction
                    step_detail["executor_thought"] = executor_thought
                    step_detail["executor_action"] = executor_action
                elif dual_instruction:
                    step_detail["dual_instruction"] = dual_instruction
                
                step_details.append(step_detail)
                
                # Check if done
                if hasattr(response, 'done') and response.done:
                    done = True
                    completion_msg = "✓ Agent signaled completion"
                    print(f"\033[32m{completion_msg}\033[0m")  # Green color
                    self.logger.log(completion_msg)
                
                print()  # Empty line after each step
                self.logger.log("")
                
                # Wait after action
                await asyncio.sleep(2.0)
            
            except Exception as e:
                error_msg = f"⚠️  Step error: {e}"
                print(f"\033[31m{error_msg}\033[0m")  # Red color
                self.logger.log(error_msg)
                import traceback
                traceback_str = traceback.format_exc()
                self.logger.log(f"Traceback:\n{traceback_str}")
                break
        
        # 🆕 For QA tasks, check interaction_cache before evaluation
        if subtask.get('requires_answer'):
            debug_msg = f"\n{'='*80}\n🔍 PRE-EVALUATION DEBUG (QA Task)\n{'='*80}"
            print(debug_msg)
            self.logger.log(debug_msg)
            self.logger.log(f"interaction_cache type: {type(self.env.interaction_cache)}")
            self.logger.log(f"interaction_cache value: {repr(self.env.interaction_cache)[:300]}")
            
            # Check the action_type of the final step
            if step_details:
                last_step = step_details[-1]
                self.logger.log(f"Last step action_type: {last_step.get('action_type')}")
                self.logger.log(f"Last step tool_call: {last_step.get('tool_call', '')[:200]}")
            
            self.logger.log(f"{'='*80}\n")
        
        # Evaluate subtask
        eval_msg = f"\n{'='*80}\n📊 Evaluating subtask...\n{'='*80}"
        print(eval_msg)
        self.logger.log(eval_msg)
        
        # 🆕 Set up logging handler to capture evaluator log output
        import io
        import sys
        
        # Create string buffer to capture logs
        log_capture_stream = io.StringIO()
        log_handler = logging.StreamHandler(log_capture_stream)
        log_handler.setLevel(logging.INFO)
        log_formatter = logging.Formatter('%(message)s')
        log_handler.setFormatter(log_formatter)
        
        # Add handler
        root_logger = logging.getLogger()
        old_level = root_logger.level
        root_logger.setLevel(logging.INFO)
        root_logger.addHandler(log_handler)
        
        # Run blocking evaluate_subtask in executor (it's not async)
        loop = asyncio.get_event_loop()
        score = await loop.run_in_executor(
            None,
            evaluator.evaluate_subtask,
            subtask_idx,
            self.env
        )
        
        # Remove handler and retrieve logs
        root_logger.removeHandler(log_handler)
        root_logger.setLevel(old_level)
        evaluation_logs = log_capture_stream.getvalue()
        log_capture_stream.close()
        
        # 🆕 Also write evaluation logs to file
        if evaluation_logs:
            self.logger.log("\n--- Evaluation Details ---")
            self.logger.log(evaluation_logs)
            self.logger.log("--- End Evaluation Details ---\n")
        
        success = score >= 0.8
        
        # 📊 Get ask statistics (if the agent supports it)
        ask_count = 0
        total_steps = step_count  # Default to execution steps
        if hasattr(agent, 'ask_count'):
            ask_count = agent.ask_count
        if hasattr(agent, 'total_steps'):
            total_steps = agent.total_steps
        
        return {
            "name": subtask.get("evaluator_name", f"Subtask {subtask_idx + 1}"),
            "success": success,
            "score": score,
            "steps": step_count,
            "step_details": step_details,  # Include detailed step information
            "details": f"Completed in {step_count} steps",
            "ask_count": ask_count,  # 📊 Add number of questions asked
            "total_steps": total_steps,  # 📊 Add total steps (including ask steps)
        }
    
