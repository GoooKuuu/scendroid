async def main_async():
    """Async main function."""
    args = parse_args()
    
    # Validate scenarios
    invalid_scenarios = [s for s in args.scenarios if s.lower() not in SCENARIO_MAP]
    if invalid_scenarios:
        print(f"❌ Invalid scenarios: {', '.join(invalid_scenarios)}")
        print(f"Valid scenarios: {', '.join(SCENARIO_MAP.keys())}")
        sys.exit(1)
    
    # Normalize scenario names
    scenarios = [s.lower() for s in args.scenarios]
    
    # Create runner
    runner = ScenarioTestRunner(
        agent_type=args.agent,
        scenarios=scenarios,
        clarity_level=args.level,
        auto_confirm=args.auto_confirm,
        console_port=args.console_port,
        grpc_port=args.grpc_port,
        start_from_subtask=args.start_from_subtask,
        reset_mode=args.reset_mode  # ⚡ Per-Task Reset mode
    )
    
    try:
        # Initialize
        await runner.initialize()
        
        # Run all scenarios
        await runner.run_all_scenarios()
        
        # Generate summary
        runner.generate_summary_report()
    
    except KeyboardInterrupt:
        print("\n\n⚠️  Interrupted by user (Ctrl+C)")
        print("📝 All logs have been saved in real-time to disk")
        # Ensure logger file handles are closed
        if hasattr(runner, 'logger') and runner.logger.log_file_handle:
            runner.logger.log("\n[INTERRUPTED] Test interrupted by user")
            runner.logger.log_file_handle.close()
            runner.logger.log_file_handle = None
        if hasattr(runner, 'logger') and runner.logger.markdown_file_handle:  # 🆕 Close markdown
            runner.logger.markdown_file_handle.close()
            runner.logger.markdown_file_handle = None
        if hasattr(runner, 'logger'):
            print(f"💾 Current log file: {runner.logger.current_log_file}")
            if runner.logger.markdown_file:
                print(f"📝 Markdown report: {runner.logger.markdown_file}")
    
    except Exception as e:
        print(f"\n\n❌ Fatal error: {e}")
        import traceback
        traceback.print_exc()
        # Ensure logger file handles are closed
        if hasattr(runner, 'logger') and runner.logger.log_file_handle:
            runner.logger.log(f"\n[ERROR] Fatal error: {e}")
            runner.logger.log_file_handle.close()
            runner.logger.log_file_handle = None
        if hasattr(runner, 'logger') and runner.logger.markdown_file_handle:  # 🆕 Close markdown
            runner.logger.markdown_file_handle.close()
            runner.logger.markdown_file_handle = None
    
    finally:
        runner.cleanup()


def main():
    """Main entry point."""
    asyncio.run(main_async())


if __name__ == "__main__":
    main()
