    def _check_layered_support(self, evaluator) -> bool:
        """Check if scenario supports layered instructions (L0/L1/L2).
        
        Args:
            evaluator: Scenario evaluator instance
        
        Returns:
            True if scenario has L0/L1/L2 instructions, False otherwise
        """
        # Check if evaluator has subtasks
        if not hasattr(evaluator, 'subtasks') or len(evaluator.subtasks) == 0:
            return False
        
        # Check first subtask for layered instructions
        first_subtask = evaluator.subtasks[0]
        has_l0 = 'user_instruction_L0' in first_subtask
        has_l1 = 'user_instruction_L1' in first_subtask
        has_l2 = 'user_instruction_L2' in first_subtask
        
        return has_l0 and has_l1 and has_l2
    
    @staticmethod
    def _find_adb_directory() -> str:
        """Returns the directory where adb is located."""
        potential_paths = [
            os.path.expanduser('~/Library/Android/sdk/platform-tools/adb'),
            os.path.expanduser('~/Android/Sdk/platform-tools/adb'),
        ]
        for path in potential_paths:
            if os.path.isfile(path):
                return path
        raise EnvironmentError('adb not found in the common Android SDK paths.')


