"""Batch testing script for Scenario-Level evaluation.

This script allows you to test a specific agent across multiple scenarios
in a specified order, with automatic log saving after each scenario.

Examples:
    # Test qwen3vl-235b on all scenarios (default order: A, B, C, D, E, Omnilife)
    python run_scenario_batch.py --agent qwen3vl-235b
    
    # Test with custom scenario order
    python run_scenario_batch.py --agent qwen3vl-235b --scenarios omnilife e d c b a
    
    # Test specific scenarios (uses original instructions by default)
    python run_scenario_batch.py --agent qwen3vl-8b --scenarios a b c
    
    # Test with specific clarity level (L0/L1/L2 for obfuscated instructions)
    python run_scenario_batch.py --agent qwen3vl-235b-context --scenarios a b --level L0
    python run_scenario_batch.py --agent qwen3vl-235b-context --scenarios a b --level L1
    
    # Auto-confirm all prompts
    python run_scenario_batch.py --agent doubao --scenarios omnilife --auto_confirm
    
    # Specify emulator ports (useful for multiple emulator instances)
    python run_scenario_batch.py --agent qwen3vl-235b --scenarios a --console_port 5554 --grpc_port 8554
    
    # Use different emulator instance
    python run_scenario_batch.py --agent qwen3vl-8b --scenarios a b --console_port 5556 --grpc_port 8556 --auto_confirm
    
    # 🎯 Fast start from specific subtask (e.g., jump to Shopping task #7)
    python run_scenario_batch.py --agent qwen3vl-235b --scenarios a --start_from_subtask 7 --auto_confirm
    
    # Useful for debugging: batch initialization runs, but execution starts from subtask 7
    python run_scenario_batch.py --agent qwen3vl-8b --scenarios omnilife --start_from_subtask 15 --auto_confirm
    
Available agents:
    Single-Model:
      - qwen3vl-235b, qwen3vl-32b, qwen3vl-8b
    
    Additional agents (dual-model, context-enhanced, ask-enabled, etc.) 
    will be available in future releases. See README.md Roadmap for details.
    
Available scenarios:
    - a : Wake-up → Work Kickoff
    - b : Busy Monday for David
    - c : Shopping Scenario C
    - d : Shopping Scenario D
    - e : Shopping Scenario E
    - omnilife : Comprehensive OmniLife Scenario

Instruction types:
    - Default (no --level): Uses original scenario instructions (user_instruction)
    - L0 (--level L0): Uses fully specified instructions (user_instruction_L0)
    - L1 (--level L1): Uses partially obfuscated instructions (user_instruction_L1)
    - L2 (--level L2): Uses highly obfuscated instructions (user_instruction_L2)
"""

