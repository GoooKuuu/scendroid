def load_scenario_evaluators():
    """Load all scenario evaluators using AppRegistry.
    
    This matches the approach used in run_layered_tui_test.py.
    Evaluators are registered in scendroid/apps/scenario/__init__.py
    """
    evaluators = {}
    
    # Import scenario modules to trigger registration
    try:
        from scendroid.apps import scenario  # noqa: F401
    except ImportError as e:
        print(f"⚠️  Failed to import scenario module: {e}")
        return evaluators
    
    # 1-day scenarios
    one_day_scenarios = [
        ('A', 'ScenarioA_BusyMonday', 'Busy Monday'),
        ('B', 'ScenarioB_RelaxedSaturday', 'Relaxed Saturday'),
        ('C', 'ScenarioC_StudentResearchDay', 'Student Research Day'),
        ('D', 'ScenarioD_TechConferenceTrip', 'Tech Conference Trip'),
        ('E', 'ScenarioE_WeekendHiking', 'Weekend Hiking & Photography'),
    ]
    
    # 7-day scenarios
    seven_day_scenarios = [
        ('Omnilife', 'ScenarioOmniLife_7Day', 'OmniLife Complete Life (7-Day)'),
    ]
    
    # Load 1-day scenarios
    for scenario_id, eval_name, display_name in one_day_scenarios:
        eval_class = AppRegistry.get_evaluator(eval_name)
        if eval_class:
            evaluators[scenario_id] = eval_class
        else:
            print(f"⚠️  {scenario_id}: {eval_name} not found in registry")
    
    # Load 7-day scenarios
    for scenario_id, eval_name, display_name in seven_day_scenarios:
        eval_class = AppRegistry.get_evaluator(eval_name)
        if eval_class:
            evaluators[scenario_id] = eval_class
        else:
            print(f"⚠️  {scenario_id}: {eval_name} not found in registry")
    
    return evaluators


